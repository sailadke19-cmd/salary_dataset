# --- Load the model ---
# Ensure the model file is in the same directory as app.py
model_path = 'random_forest_regressor_model_smaller.pkl'

if not os.path.exists(model_path):
    st.error(f"Model file not found at {model_path}. Please ensure it's in the same directory.")
else:
    try:
        model = joblib.load(model_path)
    except Exception as e:
        st.error(f"Error loading the model: {e}")
        st.stop()

# --- Load original data to fit LabelEncoders ---
# Assuming the original CSV is also available (or you have pre-saved encoders)
# For simplicity, we're assuming the CSV is present locally during deployment
# In a real-world scenario, you might save the fitted encoders or use a different strategy.
original_data_path = 'Salary_Dataset_DataScienceLovers.csv'
if not os.path.exists(original_data_path):
    st.error(f"Original dataset not found at {original_data_path}. Cannot fit LabelEncoders.")
    st.stop()

try:
    original_df = pd.read_csv(original_data_path)
except Exception as e:
    st.error(f"Error loading original dataset: {e}")
    st.stop()

# Preprocessing steps for the original_df (matching notebook)
# Fill missing 'Company Name' with mode
if 'Company Name' in original_df.columns and original_df['Company Name'].isnull().any():
    mode_company_name = original_df['Company Name'].mode()[0]
    original_df['Company Name'] = original_df['Company Name'].fillna(mode_company_name)

# Fit LabelEncoders on original categorical columns
label_encoders = {}
categorical_cols = ['Company Name', 'Job Title', 'Location', 'Employment Status', 'Job Roles']
for col in categorical_cols:
    if col in original_df.columns:
        le = LabelEncoder()
        # Fit on all unique values from the original dataset column
        le.fit(original_df[col].astype(str).unique())
        label_encoders[col] = le

# --- Input features ---

# Rating (float)
rating = st.slider("Rating", min_value=1.0, max_value=5.0, value=3.5, step=0.1)

# Company Name (categorical)
company_name_options = list(label_encoders['Company Name'].classes_)
company_name_display = st.selectbox("Company Name", company_name_options)
company_name_encoded = label_encoders['Company Name'].transform([company_name_display])[0]

# Job Title (categorical)
job_title_options = list(label_encoders['Job Title'].classes_)
job_title_display = st.selectbox("Job Title", job_title_options)
job_title_encoded = label_encoders['Job Title'].transform([job_title_display])[0]

# Salaries Reported (int)
salaries_reported = st.number_input("Salaries Reported", min_value=1, value=5, step=1)

# Location (categorical)
location_options = list(label_encoders['Location'].classes_)
location_display = st.selectbox("Location", location_options)
location_encoded = label_encoders['Location'].transform([location_display])[0]

# Employment Status (categorical)
employment_status_options = list(label_encoders['Employment Status'].classes_)
employment_status_display = st.selectbox("Employment Status", employment_status_options)
employment_status_encoded = label_encoders['Employment Status'].transform([employment_status_display])[0]

# Job Roles (categorical)
job_roles_options = list(label_encoders['Job Roles'].classes_)
job_roles_display = st.selectbox("Job Roles", job_roles_options)
job_roles_encoded = label_encoders['Job Roles'].transform([job_roles_display])[0]


# --- Prediction button ---
